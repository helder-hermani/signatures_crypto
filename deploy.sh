# Inicia Git
git ini
# Conecta ao diretório remoto
git remote add origin <URL do repositório do GitHub> 
# Adiciona ao stage e comita
git add .
git commit -m "Mensagem"
#Realiza o primeiro push
git push -u origin master

#Se der erro "Updates were rejected because the tip of your current branch is behind hint: its remote counterpart. Integrate the remote changes"
